public class LogicBooleanConstantOperator {
    public static void main(String[] args) {
        //逻辑
        System.out.println("true & false: "+(true & false));
        System.out.println("true | false: "+(true | false));
        System.out.println("true ^ false: "+(true ^ false));
        System.out.println("!false: "+(!false));
        System.out.println("!!false: "+(!!false));

    }
}
