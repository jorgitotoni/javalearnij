public class RelationOperator {
    public static void main(String[] args) {
        System.out.println("10>20="+(10>20));
        System.out.println("10<20="+(10<20));
        System.out.println("10==10="+(10==10));
        System.out.println("10!=10="+(10!=10));
        System.out.println("10>=10="+(10>=10));


    }
}
